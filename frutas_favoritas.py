frutas_favoritas = ['banana', 'maçã', 'melancia']
for fruta in frutas_favoritas:
    if fruta == 'banana':
        print('Sim banana consta na lista de frutas favoritas.')
    if fruta == 'pera':
        print('Sim pera consta na lista de frutas favoritas.')
    if fruta == 'maçã':
        print('Sim maçã consta na lista de frutas favoritas.')
    if fruta == 'manga':
        print('Não manga não consta na lista de frutas favoritas.')
    if fruta == 'melancia':
        print('Sim melancia consta na lista de frutas favoritas.')            